  /**
   * Gets ASCII art by name.
   * Available arts: "Wardkeeper's Refuge", "Aindrajalika Gate", "cleric", "summoner", "rogue", "archer", "healer", "tank", "warrior", "mage", "warlock"
   * 
   * @param name   Name of the art
   * @param width  Desired width
   * @param height Desired height
   * @return returns cropped ASCII art as a String array
   */
  static public String[] getArt(String name, int width, int height) {
    try {

      String[] art = switch (name) {
        case "Wardkeeper's Refuge" -> fire;
        case "Aindrajalika Gate" -> hardGate;
      case "cleric" -> cleric;
      case "summoner" -> summoner;
      case "rogue" -> rogue;
      case "archer" -> archer;
      case "healer" -> healer;
      case "tank" -> tank;
      case "warrior" -> warrior;
      case "mage" -> mage;
      case "warlock" -> warlock;
      default -> new String[0];
    };
    
    // If no art found, return empty array
    if (art.length == 0) {
      String[] result = new String[height];
      for (int i = 0; i < height; i++) {
        result[i] = " ".repeat(width);
      }
      return result;
    }
    
    String[] result = new String[height];
    Arrays.fill(result, " ".repeat(width));
    int paddingTop, paddingBottom, paddingLeft=art[0].length(), paddingRight;

    for (String line : art) {
      if (line.length() != paddingLeft) {
        Throwable t = new IllegalArgumentException("All lines in the ASCII art must have the same length.");
        t.printStackTrace();        
        return new String[0];
      }
    }
    
    // Calculate padding to center the art
    paddingTop = Math.round((float)(height - art.length) / 2);
    paddingLeft = Math.round((float)(width - art[0].length()) / 2);
    paddingBottom = height - paddingTop;
    paddingRight = width - paddingLeft;

    for (int i=0, j=0; i<height; i++) {
      if (i < paddingTop || i >= paddingBottom) {
        // Pad top and bottom
        result[i] = " ".repeat(width);
        continue;
      }
      String line = art[j++];
      if (paddingLeft > 0) {
        // Pad left
        line = Utils.center(line, width);
      } else {
        // Trim left
        line = line.substring(-paddingLeft, paddingRight);
      }
      result[i] = line;
    }
    
    return result;
    } catch (Exception e) {
      String[] art = new String[height];
      Arrays.fill(art, " ".repeat(width));
      return art;
    }
  }
  

