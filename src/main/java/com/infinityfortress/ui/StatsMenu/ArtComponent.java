package com.infinityfortress.ui.StatsMenu;

import com.infinityfortress.characters.NCharacter;
import com.infinityfortress.utils.Utils;

public class ArtComponent {
      public static StringBuilder display(NCharacter curr) {
        StringBuilder frame = new StringBuilder();
        frame.append("\033[H");
        String[] art = CharacterArt.art(curr.getRole().getName());
        int artSize=48;

        frame.append("\033[3;6H┌").append("─".repeat(artSize)).append("┐");
        frame.append("\033[4;6H│").append(Utils.leftAlign(" Race: "+curr.getRace().getName(), artSize)).append("│");
        frame.append("\033[5;6H│").append(Utils.leftAlign(" Role: "+curr.getRole().getName(), artSize)).append("│");
        
        for (int i=0; i<art.length; i++) {
          frame.append("\033[").append(6+i).append(";6H│").append(Utils.center(art[i], artSize)).append("│");
        }
        frame.append("\033[29;6H└").append("─".repeat(artSize)).append("┘");
        // System.out.print(frame.toString());
        return frame;
    }
}

